class SShowStudent implements Cloneable {
    private int id;
    private String name;
    private String lastName;
    private String blood;
    private String phone;
    private String city;
    private String studentClass;

    // Constructor
    public SShowStudent(int id, String name, String lastName, String blood, String phone, String city, String studentClass) {
        this.id = id;
        this.name = name;
        this.lastName = lastName;
        this.blood = blood;
        this.phone = phone;
        this.city = city;
        this.studentClass = studentClass;
    }

    // Getters and setters
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getLastName() {
        return lastName;
    }

    public String getBlood() {
        return blood;
    }

    public String getPhone() {
        return phone;
    }

    public String getCity() {
        return city;
    }

    public String getStudentClass() {
        return studentClass;
    }

    // Override clone method
    @Override
    public SShowStudent clone() {
        try {
            return (SShowStudent) super.clone();
        } catch (CloneNotSupportedException e) {
            e.printStackTrace();
            return null;
        }
    }

    @Override
    public String toString() {
        return "Student{id=" + id + ", name='" + name + "', lastName='" + lastName + "', blood='" + blood +
                "', phone='" + phone + "', city='" + city + "', class='" + studentClass + "'}";
    }
}

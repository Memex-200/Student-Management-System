   public void showRecord() {
        try {
        stm = conn.createStatement();
        String sql = "SELECT * FROM STUDENT"; // Select all columns
        rs = stm.executeQuery(sql);

        // Create a model and set it to the table
        DefaultTableModel model = new DefaultTableModel();
        table.setModel(model);

        // Add column names to the table model
        model.addColumn("ID");
        model.addColumn("First Name");
        model.addColumn("Last Name");
        model.addColumn("Blood");
        model.addColumn("Phone");
        model.addColumn("City");
        model.addColumn("Class");

        // Populate rows using the Prototype pattern
        while (rs.next()) {
            // Create a new Student object from the result set
            SShowStudent student = new SShowStudent(
                    rs.getInt("id"),
                    rs.getString("stdFirstName"),
                    rs.getString("stdLastName"),
                    rs.getString("stdBlood"),
                    rs.getString("stdPhone"),
                    rs.getString("stdCity"),
                    rs.getString("class")
            );

            // Clone the student record
            SShowStudent clonedStudent = student.clone();

            // Add the cloned student's data to the table
            model.addRow(new Object[]{
                    clonedStudent.getId(),
                    clonedStudent.getName(),
                    clonedStudent.getLastName(),
                    clonedStudent.getBlood(),
                    clonedStudent.getPhone(),
                    clonedStudent.getCity(),
                    clonedStudent.getStudentClass()
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    } finally {
        DbUtils.closeQuietly(rs);
        DbUtils.closeQuietly(stm);
    }
}


